package app;

public class Utazas {

    private final String honnan;
    private final String hova;

    public Utazas(String honnan, String hova) {
        this.honnan = honnan;
        this.hova = hova;
    }

    public String getHonnan() {
        return honnan;
    }

    public String getHova() {
        return hova;
    }
    
}
